    <h1>La créativité</h1>

    <p><span style='text-decoration: italic;'>“Pouvoir de création, d'invention.”</span> – Le Grand Sage </p>
    <p>Voilà comment les anciens décrivent ce terme, si simple et pourtant, il semble offrir une infinité de perspectives. Lorsque Artrish créa le monde il y a de cela plusieurs millénaires, la première création qu’il fit fut ce qu’on appelle les trois objets d’Artrish, aussi appelé par certains dragons, les Nograds, soit les Géants, les Humains et les Éternelles. Les Humains devaient innover, les Géants devaient façonner et les Éternelles devaient perpétuer. Dans cette gigantesque boucle, chacun avait un rôle à jouer et ce dans le but de créer quelque chose que même Artrish lui-même ne pouvait inventer seul, une civilisation. Cette civilisation créa un monde basé sur l’art. L’architecture, la cuisine, la peinture, l’écriture, la forge ou bien encore les arts martiaux.  Dans chacun de ses domaines résidaient des Nograds faisant preuve d’imagination, d’ouverture d’esprit et de créativité dans le but d’améliorer ces domaines.</p>
    <p>Évidemment, la maîtrise de flux peut être vu comme un art lui aussi, ainsi les qualités requises pour devenir un puissant mage sont similaires celles pour devenir un artiste réputé. Ce n’est pas pour rien que les plus puissants mages sont souvent aussi des artistes. L’ouverture d’esprit, la capacité à visualiser les choses, toutes ces choses s’appliquent aux flux. Certaines personnes se renferment sur leur flux pensant le connaître, mais ceci est une erreur, il arrive que l’on se rende compte qu’un flux est légèrement différent de ce que l’on pense, par exemple, un invocateur de flammes peut se révéler être un invocateur de chaleur. À l’inverse, parfois l’utilisateur se rend compte que le flux qu’il connaissait est plus grand que ce qu’il pensait. Nombre de créateurs d’eau ont réussi à créer de la glace et vice-versa, et cela sans mentionner le cas spécifique de l’éveil.</p>
    <p>C’est grâce à cela que beaucoup d'utilisateurs de flux font évoluer leur flux, le fait de s’ouvrir aux mondes qui nous entourent et d’accepter son propre pouvoir comme un ensemble libre plutôt que comme un élément fixe et immuable.</p>
    <p>Dit comme ceci, on pourrait penser que quiconque pourrait développer son flux comme il l’entend, cependant, il est difficile, voire impossible de s’auto convaincre de quelque chose que nous croyons faux dans le seul but de développer notre magie. Généralement, lorsque l’on s’en rend compte, il est déjà trop tard. C’est ici qu’une vision artistique du flux est souvent nécessaire pour outrepasser ce blocage mental, mais même ceci est réservé qu’à quelques élus.</p>
    <p>C’est ainsi que le Grand Sage prend un point de vue intéressant indiquant que selon lui, “L’innocence est la plus grande vertue humaine”. En ne connaissant rien, en étant limité dans notre sagesse, nous sommes sans limite de flux. Selon lui si Artrish à empêché les Nograds enfants d'utiliser leur flux, c’est car “Seul un nouveau né serait assez fou pour l’affronter (En parlant d’Artrish)”.</p>
