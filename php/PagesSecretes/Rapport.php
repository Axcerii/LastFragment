        <h1>Rapport Hameau des prés verts</h1>
        <h3>Date du rapport : 1er Chronos</h3>
        <h3>Soldat chargé de l’enquête : Sero</h3>

        <p>Nous sommes arrivés au Hameau des prés verts juste après le rapport de mission de Destra et Leest, soit le 30e Lianos. D’après ce rapport, nous devrions retrouver deux groupes distincts, un vers l’Ouest du hameau et un à l’intérieur. En ce qui concerne les personnes dans l’enceinte du hameau, les descriptions collent quasiment toutes. Et Leaf y a effectué un sacré massacre. La majorité ont été tués sur le coup par un fil traversant leur boîte crânienne. Nous avons aussi vu deux personnes qui semblaient s'apprêter à l’attaquer, mais au vu de l’absence de sang sur leurs armes, ils ont sans doute été tués avant d’avoir effleuré Leaf. Toujours comme décrit dans son rapport, il y a la brigande recherchée pour meurtre, Yinshi, qui semble avoir eu la jambe cassée et qui a été retrouvée et tuée par Leaf.</p>
        <p>Il manque cependant les corps de la fille qui se prénommerait “Alu”, une femme aux longs cheveux bleus, “Puda”, une femme blonde aux yeux bleus, et de “Poto”, un trentenaire mal entretenu aux cheveux noirs mi-courts et aux yeux fatigués. Soit ils ont réussi à s’enfuir, soit, comme à son habitude, Leaf les a recrutés et on n’entendra pas parler d’eux avant un long moment.</p>
        <p>Nous avons retrouvé le cadavre de la brigande primée Sellu, à l’endroit indiqué par Destra. Le corps est identifiable comme étant le sien, la prime sera donc versée au groupe.</p>
        <p>Après investigation à l’Ouest, nous avons retrouvé trois cadavres quasiment inidentifiables. La seule chose que nous pouvons dire, c’est qu’aucun ne correspond à Zarashi au vu de leur corpulence. Le brigand primé a lui aussi sans doute été recruté par Leaf.</p>
        <p>Nous avons réalisé des investigations dans les hameaux alentour. “Poto” aurait été vu au hameau de la Roche Lisse. Il existe donc bien un témoin de ce qui s’est passé là-bas. Ce serait une première pour Leaf, nous devons le retrouver ; il a peut-être des informations importantes pour vaincre les Guignols.</p>
        <p>Je vous envoie ce début de rapport par le biais d’un messager. Je pars à la recherche de ce “Poto”. Je tâcherai de revenir pour la réunion liée à la guerre contre le groupe Sugawara. Je le chercherai seul pour éviter d’être ralenti.</p>
        <h3>Fin de rapport.</h3>
