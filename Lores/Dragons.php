    <h1>Dragons</h1>
    <br>
    <p>Les dragons est l’une des quatres races peuplant le monde d’Aeteria. Parmi toutes les races, il s’agit de la race la plus complexe et la plus importante du monde.
</p>

    <h2>Physionomie</h2>
    <p>
    Les dragons possèdent un physique proche des géants. Tout comme ces derniers, leur peau est très rigide, elle est épaisse et lourde. Cela permet d’encaisser les attaques les plus faibles, comme des poignards mal aiguisés ou encore des flèches tirés à de grandes distances. Il possède une force ainsi qu’une aisance à porter de lourdes charges. Leur taille varie entre 5 et 6 coudées. <br>
À cela s’ajoute le fait que les dragons vieillissent plus lentement à l’instar des éternelles. Ils ne semblent jamais atteindre une forme dite adultes, cela est probablement dû à l’absence de sexe chez eux. Aucun texte ne fait mention de leur système pour rejeter les déchets de leur corps, il est théorisé qu’ils possèdent le même système que les éternelles mais cela n’a jamais été confirmé.
<br> <br>
Leur corps est jugé comme étant la forme parfaite. Il possède les avantages physiques des géants, la longévité des éternelles et le flux des humains.
    </p>

    <h2>Flux</h2>
    <h3>Flux canalisable</h3>
    <p>
    Les dragons possèdent une quantité de flux canalisable phénoménale. En plus de posséder une quantité de flux naturellement supérieure aux humains, à cela s’ajoute leur longévité qui leur permet d'accroître et d'entraîner leur maîtrise.
	<br>À l’aide de leur corps physiquement plus résistant, les dragons peuvent se permettre de canaliser du flux pure, ils ressentiront un contrecoup, mais non léthal.

   </p>
   <h3>Corps de flux</h3>
   <p>Le corps de flux des dragons est composé de la même façon que celui des géants, c'est-à-dire qu'il est auto-régénérant, en cas de blessure sur le corps physique, le corps de flux retiré, va, après un certain temps, finir par se régénérer, ce qui entraînera la recréation du corps physique.</p>
    
   <h3>Héritage</h3>
   <p>L’héritage des dragons est un phénomène complexe qui nous fut expliqué par les fondateurs de Mudan.
	<br>Les dragons se reproduisent par échange de flux, comme les éternelles. Une fois l’échange effectué, deux enfants naissent. Chacun représente un des Dragons. Cet enfant est alors nommé “héritier” et on lui accorde la même dénomination que son parent. L’héritier ne possède pas l’intégralité du pouvoir d’un Dragon. Ce dernier recevra l’entièreté de ses pouvoirs lorsque son parent succombera. On parle alors d’héritage.
<br>Toujours selon Lada et Pestia, il n’est pas possible d’avoir plusieurs héritiers vivant simultanément.
</p>
<h3>Attributs</h3>
<p>Les attributs sont des parts de pouvoir que possèdent les dragons. Chaque dragon en possède un certain nombre limité. Ils peuvent alors, donner une partie de leur corps de flux à un être vivant, ce dernier recevra le corps de flux, le corps physique et si le dragon en a envie, il peut rattacher un attribut à cette partie du corps. À ce moment-là, l’être vivant en question possède une partie des pouvoirs dudit Dragon.
<br>	Nous n’avons quasiment aucune information sur les attributs. Les seules informations proviennent globalement des écrits de Lada à ce sujet.
</p>
   <h2>Dans l’histoire</h2>
    <p>Les dragons ont un impact titanesque sur l’histoire de ce monde. De par leur puissance bien supérieure aux autres races, les dragons se sont vite retrouvés désignés au rôle de leader. Beaucoup de pays sont dirigé par des Dragons ; L’impératrice de la lumière, Pestia à Mudan accompagné du Sage des Tempêtes, Lada, La “Déesse” Pura à son royaume éponyme, anciennement, Guizamark, Gardien de la Vie à Edenia ou encore le Forgeron des Mondes, Goliath, sur les contrées nordiques pour rester dans les anciens souverains.
	<br>Même s’ils ne sont pas des leaders beaucoup de Dragon se sont démarqués par des hauts faits ou encore la formation du monde actuel, et ceux sans parler des dragons originelles. 
<br>
<br>Parmi les dragons diverses catégories sont présentes, pour plus d’informations, veuillez consulter les pages associées. 
</p>

