    <h1>Éternelles</h1>
    <br>
    <p>Les Éternelles est l’une des quatres races peuplant le monde d’Aeteria. Il s’agit de la race la moins commune parmi les Nograd (Non-Dragon). Elle peuple un certain archipel de Mudan, de plus elle représente une petite partie de la population actuelle Edenienne.
</p>

    <h2>Physionomie</h2>
    <p>
    Les éternelles ne possèdent pas de particularité physique, ils sont globalement plus faibles que la moyenne à cause du fait qu’ils vieillissent lentement, leur espérance de vie tourne autour des 200 ans. Ils ne semblent pas recevoir de puberté, sûrement dû à l’absence de partie génitale chez eux. Leur méthode de reproduction est directement liée à leur flux canalisable.
   <br> <br>
   Les éternelles sont assez compliqués à distinguer à l'œil nu d’un humain, ils semblent juste plus jeunes et plus petits.     
</p>

    <h2>Flux</h2>
    <h3>Flux canalisable</h3>
    <p>
    Les Éternelles ont souvent un flux canalisable plus faible que la moyenne humaine. Certains théorisent que cela serait dû à leur petite taille, où encore dû à l’évolution de leur flux.
   </p>
   <h3>Corps de flux</h3>
   <p>
   Leurs corps de flux ne possèdent pas de particularité spéciale. Cependant, ils semblent possible de générer un corps de flux en mélangeant deux flux canalisables différents, c’est comme cela que fonctionnent la reproduction chez les Éternelles. Cela à pour effet de créer deux nouveaux individus, ayant des caractéristiques similaires aux donneurs de flux.
</p>
    
   <h3>Évolutions ?</h3>
   <p>
    Les éternelles possèdent souvent, surtout chez les Édeniens, des flux particulièrement étranges et puissants. On raconte que ce serait dû à une sélection naturelle effectuée durant l’évolution. Étant plus faible physiquement et ayant une faible quantité de flux, les seules éternelles ayant survécu possédaient des flux puissants, aidant à la survie.
    </p>
   <h2>Dans l’histoire</h2>
   <p>
       Les éternelles n’ont hélas pas connu une belle histoire. Car si à Mûdan les éternelles ont plutôt bien été accueilli par les deux dragons de la météo, du côté des dragons vitaux, les éternelles ont connu un véritable désastre. Dans le pays d’Edenia, un génocide a eu lieu, qui a durée une centaine d’années. Le pourquoi n’a jamais été relevé, des milliers d’éternelles ont simplement été tués par des habitants d'Edenia à une époque où aucun souverain régnait sur ces terres. Lorsque Guizamark prit le pouvoir. Il mit fin au désastre, mais les dégâts avaient déjà été faits. Le pays a alors vécu dans un racisme constant à l’encontre de ces êtres qui se perpétue de génération en génération. 
   </p>
