    <h1>Humains</h1>
    <br>
    <p>
    Les Humains est l’une des quatres races peuplant le monde d’Aeteria. Il s’agit du peuple le plus commun de ce monde. Le seul pays où les humains sont en minorité est Saxifra, les peuples du Nord, composé en majorité de Géants.
</p>

    <h2>Physionomie</h2>
    <p>
    Nous nous servons des humains pour baser notre “Norme”, il est donc compliqué de les décrire sans point de comparaison.
</p>

    <h2>Flux</h2>
    <h3>Flux canalisable</h3>
    <p>
    Les humains est l’espèce Nograd (Non-Dragon) possédant le plus de flux canalisable. L’identité du flux d’un individu est déterminée à la naissance mais se dévoile entre 3 et 6 ans. Elle est soit hérité d’un membre de la famille, soit complètement aléatoire. Personne ne sait comment fonctionne la généalogie d’une identité de flux.
   </p>
   <h3>Corps de flux</h3>
   <p>
   Les humains possèdent un corps de flux assez faible, laissant passer beaucoup de flux canalisables. Ce dernier ne se régénère pas et peut-être consommé lorsqu’un humain dépasse ses limites, rendant certaines parties du corps de l’humain inutilisable définitivement. Cet effet est appelé “contrecoup d’adrénaline”. La maîtrise de son corps de flux est utilisée par les Invocateurs et se prénomme “Maîtrise des portails” en référence à l’étrange phénomène visuel que les invocateurs créent lorsqu'ils ouvrent leurs réalités personnelles.
</p>
    
   <h2>Dans l’histoire</h2>
   <p>
