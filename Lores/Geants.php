    <h1>Géants</h1>
    <br>
    <p>
    Les Géants est l’une des quatres races peuplant le monde d’Aeteria. Elle peuple une majorité de Saxifra ainsi qu’une bonne partie d’Irysia, de plus certains groupes étant nomades, il existe aussi pas mal de groupes de Géants à travers le monde. Il est assez commun d’en croiser à Augeaime par exemple.
</p>

    <h2>Physionomie</h2>
    <p>
    Les géants comme l’indiquent leurs noms, possèdent une taille largement supérieure à celle des humains. Encore plus que les dragons, ils possèdent une taille dépassant souvent les 5 coudées, y compris chez les géantes.
   <br> <br>
   À cela s’ajoute le fait qu’ils possèdent une peau bien plus épaisse que la moyenne, et une force brute toujours supérieure à celle du plus puissant des humains. Lada s’est déjà prononcé sur le fait d’être plus faible physiquement que certains soldats géants saxifrancs.
   
   <br> <br>
   Physiquement les géants sont des monstres de muscles et sont de redoutables soldats immuable au chaos de la naissance.
</p>

    <h2>Flux</h2>
    <h3>Flux canalisable</h3>
    <p>
    Les géants ne possèdent pas de flux canalisable, ce qui les différencie des humains.
   </p>
   <h3>Corps de flux</h3>
   <p>
   Contrairement à beaucoup d'idées reçues, les géants possèdent bien un corps de flux, qui plus est bien supérieur à celui de tout autre espèce Nograd. Leurs corps de flux est si dense qu’il ne peut générer de flux canalisable, cependant ce dernier se régénère, comme celui d’un lézard ou d’un dragon leur donnant en plus d’un physique enviable, une capacité de régénération impressionnante. Pour un bras coupé au niveau du centre de l’avant-bras, environ un an est estimé avant d’en récupérer un parfaitement utilisable.
</p>
    
   <h3>Attributs</h3>
   <p>
   Tout comme un dragon pourrait le faire, un géant peut donner une partie de son corps à un autre individu. Que ce soit pour combler un organe manquant, ou un membre coupé, si le donneur est compatible avec le receveur un don est parfaitement possible. Cependant contrairement aux dragons, l’attribut donné ne s’adapte pas au corps, ce qui complique les dons de géants à humains.
    </p>
   <h2>Dans l’histoire</h2>
   <p>
   Les géants ont connu une histoire assez spéciale. Contrairement aux éternelles, les géants n’ont pas été victimes d’un racisme aussi violent, mais certains humains ne les portaient pas dans leur cœur. Si à Irysia les humains et les géants arrivent à vivre dans une bonne entente, à Saxifra, la domination géante est indiscutable, ils règnent en maître, ne jurant que par la puissance physique depuis que le Roi Edrahil 1er a remporté la guerre civile.
   </p>
